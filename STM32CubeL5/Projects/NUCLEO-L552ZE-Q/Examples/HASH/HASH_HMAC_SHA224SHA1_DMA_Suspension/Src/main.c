  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_DMA_Init();
  MX_ICACHE_Init();
  MX_HASH_Init();
  /* USER CODE BEGIN 2 */
  /****************************************************************************/
  /*                                                                          */
  /*       DMA-based HMAC SHA-224 processing suspension/resumption            */
  /*                                                                          */
  /****************************************************************************/
  process_ongoing = 1;      /* Flag indicating a block processing is on-going     */
  iteration_counter = 1;    /* Number of iterations (number of blocks processed ) */
  block_length = 1200;      /* Process the input data as a sequence of smaller blocks
                               each of size 1200 bytes, (except the last one that
                               may be smaller).
                               This is used only to demonstrate the multi-buffer
                               HMAC suspension mechanism. */
  step = HMAC_FIRST_BUFFER; /* Step variable is set to process the first block */

  input_data = aInput_LowPrio_Large;  /* input_data points at the beginning of the input buffer */

